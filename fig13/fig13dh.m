function delayf = ddelagsf(tau_0,N)

delayf = @ddelags;

function delay = ddelags(t,epsilon)
delay = zeros([1 2*N^3]);
for m = 1:N
    for n = 1:N
        x = (2*m-2)*N+n;   
        y = x+N;           
        delay(1,x) = t-abs(m-n)*tau_0;   
        delay(1,y) = t-(m+n)*tau_0;      
    end
end
delay = delay';
end

end


